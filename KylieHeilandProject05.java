import java.util.Scanner; 
public class KylieHeilandProject05 
{
    public static void main(String[] args) 
    {
        Scanner input = new Scanner(System.in);
        System.out.print("Enter the amount to borrow (principal): ");
        double principal = input.nextInt();
        
        System.out.print("Enter the term in years: ");
        int years = input.nextInt();
        
        System.out.print("Enter the Quoted Interest Rate in decimal: ");
        double interestRate = input.nextDouble();
        
        double r = interestRate /12;
        System.out.println("Month          Payment          Total Interest          Balance");
        
        for(int i = 1; i <= years * 12; i++){
            double monthlyPay = principal * r * Math.pow(1 + r, (years * 12)) / (Math.pow(1 + r, (years * 12)) -1); //Calculates the monthly payment
            double b = (Math.pow(1 + r, i) * principal) - (((Math.pow(1 + r, i) - 1) * monthlyPay)/r); // Balance
            double interest = (monthlyPay * i) - (principal - b); // Total interest
            
            System.out.printf("%5d", i); //Prints out the months
            
            System.out.printf("%8c", '$'); //Prints out the dollar signs
            System.out.printf("%9.2f", + monthlyPay); //Prints out the monthly payment
            
            System.out.printf("%8c", '$'); //Prints out the dollar signs
            System.out.printf("%12.2f", + interest); // Prints out the Total Interest
            
            System.out.printf("%9c", '$'); //Prints out the dollar signs
            System.out.printf("%15.2f", b); //Prints out the Balance
            
            System.out.println(); //Moves to a new line
        }        
     }
   }
   
